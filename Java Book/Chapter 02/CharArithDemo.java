// Character variables can be handled like integers.
class CharArithDemo {
    public static void main(String args[]) {
        char ch;

        ch = 'X';
        System.out.println("ch contains " + ch);

        ch++;   // Increment ch
        System.out.println("ch is now " + ch);

        ch = 90;    // Gives ch the value of 'Z'
        System.out.println("ch is now " + ch);
    }
}
